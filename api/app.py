        if os.path.exists(local_path):
            use_local = True
            try:
                df = read_dataset(open(local_path, "rb"), local_path)
                st.success(f"Loaded local file: {local_path} ({len(df)} rows)")
            except Exception as e:
                st.error(f"Failed to load local file: {e}")
