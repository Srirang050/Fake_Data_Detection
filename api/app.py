# -------- Helpers --------
def read_dataset(uploaded_file: io.BytesIO, filename: str) -> pd.DataFrame:
    name = filename.lower()
    uploaded_file.seek(0)
    if name.endswith(".csv"):
        return pd.read_csv(uploaded_file, dtype=str, encoding='utf-8', low_memory=False)
    if name.endswith(".tsv") or name.endswith(".txt"):
        return pd.read_csv(uploaded_file, sep="\t", dtype=str, encoding='utf-8', low_memory=False)
    if name.endswith(".json") or name.endswith(".jsonl"):
        return pd.read_json(uploaded_file, lines=True)
    # fallback: try csv
    try:
        return pd.read_csv(uploaded_file, dtype=str, encoding='utf-8', low_memory=False)
    except Exception:
        uploaded_file.seek(0)
        text = uploaded_file.read().decode('utf-8', errors='ignore')
        return pd.DataFrame({"text": [text]})

def simple_clean(s: str) -> str:
    if s is None:
        return ""
    s = str(s)
    s = re.sub(r"<[^>]+>", " ", s)  # strip HTML tags
    s = re.sub(r"https?://\S+|www\.\S+|\S+@\S+", " ", s)  # strip urls/emails
    s = re.sub(r"\s+", " ", s).strip()
    return s

def detect_text_column(df: pd.DataFrame) -> List[str]:
    # prefer 'text' columns, otherwise long string columns
    candidates = []
    lower = [c.lower() for c in df.columns]
    if 'text' in lower:
        idx = lower.index('text')
        candidates.append(df.columns[idx])
    # add columns with many long strings
    lengths = {c: df[c].astype(str).map(len).median() for c in df.columns}
    # sort by median length desc
    sorted_cols = sorted(lengths.items(), key=lambda kv: kv[1], reverse=True)
    for c, _ in sorted_cols[:3]:
        if c not in candidates:
            candidates.append(c)
    return candidates

def keyword_leak_score(text: str, keywords: List[str]) -> int:
    t = text.lower()
    for k in keywords:
        if k in t:
            return 1
    return 0

def repetition_ratio(text: str) -> float:
    toks = text.split()
    if not toks:
        return 0.0
    c = Counter(toks)
    most_common = c.most_common(1)[0][1]
    return most_common / max(1, len(toks))

def heuristic_synthetic_score(series: pd.Series) -> np.ndarray:
    """
    For each text produce a score 0..1 indicating likelihood synthetic based on heuristic rules:
    - presence of "generated" tokens, model names, or html artifacts (strong signal)
    - presence of urls/html/email (weak signal)
    - extremely short or extremely long (weak signal)
    - repetition ratio high (weak signal)
    Weighted sum normalized to [0,1].
    """
    keywords = ["generated by", "generated", "gpt", "llama", "openai", "davinci", "chatgpt", "gpt-3", "gpt-4",
                "synth", "__synth__", "synthetic", "ai generated", "generated_text", "generator"]
    kw_arr = series.fillna("").astype(str).map(lambda s: keyword_leak_score(s, keywords)).to_numpy(dtype=float)
    html_arr = series.fillna("").astype(str).map(lambda s: 1.0 if re.search(r"<\/?\w+>", s) else 0.0).to_numpy()
    url_arr = series.fillna("").astype(str).map(lambda s: 1.0 if re.search(r"https?://|www\.", s) else 0.0).to_numpy()
    rep_arr = series.fillna("").astype(str).map(repetition_ratio).to_numpy()
    lens = series.fillna("").astype(str).map(len).to_numpy(dtype=float)
    # length score: very short (<20) or very long (>2000) may be synthetic or truncated - use logistic-ish scoring
    len_score = np.clip((lens - 20) / (200 - 20), 0, 1)
    len_score = 1 - np.abs(len_score - 0.5) * 2  # peak in middle -> lower score, extremes higher
    # combine
    # weights tuned for quick feedback; later replace with ML model
    score = (kw_arr * 0.5) + (html_arr * 0.15) + (url_arr * 0.05) + (rep_arr * 0.15) + (len_score * 0.15)
    # normalize to [0,1]
    score = np.clip(score, 0.0, 1.0)
    return score

def load_sklearn_model(uploaded_bytes: io.BytesIO):
    # expects a joblib or pickle containing an object with .predict or .predict_proba that accepts list[str]
    try:
        uploaded_bytes.seek(0)
        model = joblib.load(uploaded_bytes)
        return model
    except Exception as e:
        st.error(f"Failed to load sklearn model: {e}")
        return None

def predict_with_sklearn(model, texts: List[str]) -> np.ndarray:
    # Try predict_proba otherwise predict
    try:
        if hasattr(model, "predict_proba"):
            probs = model.predict_proba(texts)
            # handle 2d -> class1 probability
            if probs.ndim == 2 and probs.shape[1] >= 2:
                return probs[:, 1]
            # fallback: use max column
            return np.max(probs, axis=1)
        else:
            preds = model.predict(texts)
            return np.array(preds, dtype=float)
    except Exception as e:
        st.warning(f"sklearn model prediction failed: {e}")
        return np.zeros(len(texts), dtype=float)

def load_hf_model_from_dir(model_dir: str):
    # loads HF model and tokenizer from a directory path
    if not _TRANSFORMERS_AVAILABLE:
        st.error("transformers/torch not available in this environment.")
        return None, None
    try:
        tokenizer = AutoTokenizer.from_pretrained(model_dir, use_fast=True)
        model = AutoModelForSequenceClassification.from_pretrained(model_dir)
        return tokenizer, model
    except Exception as e:
        st.error(f"Failed to load HF model from {model_dir}: {e}")
        return None, None

def predict_with_hf(tokenizer, model, texts: List[str], device: Optional[str]=None) -> np.ndarray:
    import torch.nn.functional as F
    if device is None:
        device = "cuda" if torch.cuda.is_available() else "cpu"
    model.to(device)
    model.eval()
    all_probs = []
    batch_size = 16
    for i in range(0, len(texts), batch_size):
        batch = texts[i:i+batch_size]
        enc = tokenizer(batch, truncation=True, padding=True, return_tensors="pt")
        enc = {k: v.to(device) for k, v in enc.items()}
        with torch.no_grad():
            out = model(**enc)
            logits = out.logits
            probs = F.softmax(logits, dim=1)[:, 1].cpu().numpy()
            all_probs.extend(probs.tolist())
    return np.array(all_probs, dtype=float)

