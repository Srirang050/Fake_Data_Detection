# -------- Run analysis when asked -------
if 'run_btn' in locals() and run_btn and df is not None:
    st.subheader("Results")
    # Prepare dataframe copy
    df_proc = df.copy()
    # ensure chosen col exists
    if chosen not in df_proc.columns:
        st.error("Chosen text column not found.")
    else:
        # cleaning
        st.info("Cleaning text...")
        df_proc['__text_raw'] = df_proc[chosen].astype(str)
        df_proc['__text'] = df_proc['__text_raw'].map(simple_clean)

        # dedupe exact
        if dedup_exact:
            before = len(df_proc)
            df_proc = df_proc.drop_duplicates(subset=['__text']).reset_index(drop=True)
            st.write(f"Dropped {before - len(df_proc)} exact duplicate rows.")

        # near dedupe (naive O(n^2))
        if dedup_near:
            st.info(f"Running near-dedup at threshold {dedup_near_thresh} (this can be slow)...")
            # naive; for thousands it's borderline; we warn
            texts = df_proc['__text'].tolist()
            keep_idx = []
            from difflib import SequenceMatcher
            for i, t in enumerate(texts):
                skip = False
                for j in keep_idx:
                    sim = SequenceMatcher(None, t, texts[j]).ratio()
                    if sim >= dedup_near_thresh:
                        skip = True
                        break
                if not skip:
                    keep_idx.append(i)
            df_proc = df_proc.iloc[keep_idx].reset_index(drop=True)
            st.write(f"After near-dedup: {len(df_proc)} rows")

        st.write("Dataset size for analysis:", len(df_proc))

        # run heuristic scoring
        st.info("Computing heuristic synthetic score...")
        scores = heuristic_synthetic_score(df_proc['__text'])
        df_proc['heuristic_score'] = scores

        # if sklearn model uploaded, use it
        use_model = False
        probs = None
        if sk_model is not None:
            st.info("Predicting with uploaded sklearn model...")
            try:
                texts = df_proc['__text'].astype(str).tolist()
                probs = predict_with_sklearn(sk_model, texts)
                # if predict returned integer labels, convert to 0/1 probabilities
                if probs.dtype.kind in 'iu':
                    probs = probs.astype(float)
                # normalize to 0..1 if necessary
                if probs.max() > 1.0 or probs.min() < 0.0:
                    probs = (probs - probs.min()) / max(1e-8, (probs.max() - probs.min()))
                df_proc['model_prob'] = probs
                use_model = True
                st.success("sklearn model predictions added.")
            except Exception as e:
                st.warning(f"sklearn prediction error: {e}")

        elif hf_model is not None and hf_tokenizer is not None:
            st.info("Predicting with provided HuggingFace model...")
            try:
                texts = df_proc['__text'].astype(str).tolist()
                probs = predict_with_hf(hf_tokenizer, hf_model, texts)
                df_proc['model_prob'] = probs
                use_model = True
                st.success("HF model predictions added.")
            except Exception as e:
                st.warning(f"HF model prediction error: {e}")

        # final probability - prefer model_prob if available else heuristic
        if 'model_prob' in df_proc.columns:
            df_proc['final_prob_synth'] = df_proc['model_prob']
            st.write("Using uploaded model for final probability.")
        else:
            # smooth combine heuristic with a pseudo-model from TF-IDF+logreg if sklearn exists and no model uploaded
            if _SKLEARN_AVAILABLE and (not use_model):
                st.info("Training a lightweight TF-IDF+LogReg internal model on-the-fly (unsupervised fallback uses heuristic labels) ...")
                # create weak labels from heuristic to bootstrap quick classifier
                weak_labels = (df_proc['heuristic_score'] > 0.5).astype(int)
                try:
                    vect = TfidfVectorizer(max_features=20000, ngram_range=(1,2))
                    X = vect.fit_transform(df_proc['__text'].astype(str).tolist())
                    clf = LogisticRegression(max_iter=500)
                    clf.fit(X, weak_labels)
                    probs = clf.predict_proba(X)[:,1]
                    df_proc['tfidf_prob'] = probs
                    # combine heuristic and tfidf weak model
                    df_proc['final_prob_synth'] = (df_proc['heuristic_score'] * 0.45) + (df_proc['tfidf_prob'] * 0.55)
                    st.success("Lightweight model trained and combined with heuristic.")
                except Exception as e:
                    st.warning(f"Lightweight model failed: {e}. Falling back to heuristic alone.")
                    df_proc['final_prob_synth'] = df_proc['heuristic_score']
            else:
                df_proc['final_prob_synth'] = df_proc['heuristic_score']
                st.write("Using heuristic score as final probability (no model provided).")

        # classify: threshold slider
        st.subheader("Classification threshold")
        thresh = st.slider("Probability threshold for 'synthetic' class", min_value=0.0, max_value=1.0, value=0.5, step=0.01)
        df_proc['pred_label'] = (df_proc['final_prob_synth'] >= thresh).astype(int)

        # Summary numbers
        total = len(df_proc)
        synth_count = int(df_proc['pred_label'].sum())
        real_count = total - synth_count
        synth_pct = synth_count / total * 100.0
        real_pct = real_count / total * 100.0

        st.metric("Total rows analyzed", total)
        st.metric("Estimated synthetic (%)", f"{synth_pct:.2f}%")
        st.metric("Estimated real (%)", f"{real_pct:.2f}%")

        # Charts
        st.subheader("Charts")
        chart_df = pd.DataFrame({
            "label": ["real", "synthetic"],
            "count": [real_count, synth_count]
        })
        st.bar_chart(data=chart_df.set_index("label"))
        st.write("Distribution pie (approx):")
        st.pyplot(
            __import__("matplotlib").pyplot.figure(figsize=(4,4))
            or None
        )  # placeholder to ensure matplotlib available below

        import matplotlib.pyplot as plt
        fig, ax = plt.subplots(figsize=(4,4))
        ax.pie([real_count, synth_count], labels=["real", "synthetic"], autopct="%1.1f%%", colors=None)
        ax.set_title("Estimated composition")
        st.pyplot(fig)

        # Top suspicious rows (highest final_prob)
        st.subheader("Top suspicious (highest synthetic probability)")
        topk = st.number_input("How many top suspicious rows to show", min_value=3, max_value=100, value=10, step=1)
        top = df_proc.sort_values("final_prob_synth", ascending=False).head(int(topk))
        # show a small table (raw + probability)
        display_cols = ['__text_raw', 'final_prob_synth', 'pred_label']
        for c in df_proc.columns:
            # keep original columns if user wants to see them
            if c not in display_cols and c in df.columns:
                display_cols.insert(0, c)
        st.dataframe(top[display_cols].rename(columns={'__text_raw': 'text'}), height=300)

        # Leakage report
        st.subheader("Leakage & token checks (quick)")
        leak_keywords = ["generated", "gpt", "openai", "llama", "generated_by", "synthetic", "__synth__", "source:"]
        leak_counts = {}
        for k in leak_keywords:
            leak_counts[k] = int(df_proc['__text'].astype(str).str.contains(k, case=False, na=False).sum())
        st.write("Counts for suspicious tokens/markers:")
        st.json(leak_counts)

        # If label column existed, show confusion-like quick check
        if label_col != "(none)":
            true_col = label_col
            # ensure numeric
            try:
                df_proc['_true_label'] = df_proc[true_col].astype(int)
                from sklearn.metrics import classification_report
                report = classification_report(df_proc['_true_label'], df_proc['pred_label'], output_dict=True)
                st.subheader("If ground-truth label provided — quick report")
                st.json(report)
            except Exception as e:
                st.info("Could not compute ground-truth comparison (label column not integer / incompatible).")

        # allow download
        st.subheader("Download annotated file")
        out_path = st.text_input("Output filename (download local browser copy)", value="results/predictions_inspected.csv")
        buf = df_proc.copy()
        # put final fields at end for readability
        if '__text' in buf.columns and '__text_raw' in buf.columns:
            pass
        # write CSV to buffer
        csv_bytes = buf.to_csv(index=False).encode('utf-8')
        st.download_button("Download CSV with predictions", data=csv_bytes, file_name=os.path.basename(out_path), mime="text/csv")
        st.success("Done. You can now inspect suspicious rows and download results.")
